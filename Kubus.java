package BangunRuang;

public class Kubus {
    // Metode untuk menghitung volume kubus
    public double hitungVolume(double sisi) {
        return sisi * sisi * sisi;
    }

    // Metode untuk menghitung luas permukaan kubus
    public double hitungLuasPermukaan(double sisi) {
        return 6 * sisi * sisi;
    }
}